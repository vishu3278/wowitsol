<script type="text/javascript">
  $(document).ready(function(){
    axios.get('<?php echo HOST_NAME; ?>alpha/projects_api.php')
    .then(res => {
      if (res.status == 200) {
        let template = '';
        res.data.forEach((v, i)=>{
          template += `<div class="item"><a href="<?php echo HOST_NAME; ?>image/clients/${v.image}" class="ui fluid image chocolat-image"><img src="<?php echo HOST_NAME; ?>image/clients/${v.image}" alt="${v.project}"></a></div>`;
        })
        $("#portfolio").html(template);
        initPortfolio();
      } else {
        alert(res.statusText + " Some error occurred!")
      }
    })
    .catch(err => alert(err));

    function initPortfolio() {
      $("#portfolio").slick({
        'autoplay':true,
        arrows: true,
        'slidesToShow':3,
        'mobileFirst':false,
        // 'adaptiveHeight':true,
        'dots': true,
        centerMode: true,
        'responsive':[
          {breakpoint: 1020,settings: {
            'slidesToShow': 3}},
          {breakpoint: 801,settings: {
            arrows: false,
            slidesToShow: 2,
          }}, 
          {breakpoint: 561,settings: {
            arrows: false,
            slidesToShow:1
          }}
          ]
      });
    }

      $(".cliets-slide").slick({
        'autoplay':true,
        'slidesToShow':4,
        'arrows':false,
        'dots':false,
        'adaptiveHeight':true,
        'responsive':[{
          breakpoint:1024,settings:{
            'slidesToShow': 4}},
          {breakpoint:640,settings:{
            'slidesToShow': 2}}]
        });
      
      $('#portfolio a.chocolat-image').simpleLightbox();
  })
</script>
